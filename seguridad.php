                    </div>
                    <p class="u-margin-bottom-med">Catedral Alta Patagonia cuenta con un grupo de entre 30 y 40 personas, que realizan la apertura de pistas, evalúen el riesgo de avalancha, preparan la señalización en la montaña, se encarga de la colocación de todos los elementos de protección y atienden a todas aquellas personas que sufren algún tipo de accidente o cualquier incidente que pudiera ocurrir como, por ejemplo, el rescate en medios de elevación o las búsquedas nocturnas. Todos ellos están altamente capacitados y formados para los trabajos designados, muchos de ellos hacen doble temporada en Europa y constantemente todo el Equipo de Patrulla hace cursos de actualización permanente, lo cual les permite contar con las técnicas y materiales de vanguardia en prevención y rescate en accidentes.</p>
                    <p class="u-margin-bottom-med"><span class="u-color-black">¿Qué hacen?</span> Los patrulleros son los primeros en subir a la montaña, y los últimos en bajar. Por la mañana, ascienden junto con los operarios de los medios de elevación, recorren las pistas y las balizan para que, una vez habilitadas al público, los esquiadores puedan esquiar en un lugar seguro y terminar la jornada con un grato recuerdo de la estación de esquí.</p>
                    <p class="u-margin-bottom-med">Asimismo, atienden a todo esquiador en problemas y, en caso de ser necesario, le brindan los primeros auxilios y realizan las evacuaciones hasta la Base, donde es derivado a los servicios asistenciales.</p>
                    <p>Al final del día, cuando llega el momento de cerrar los medios, los patrulleros cierran las pistas, cuidando que no quede ningún rezagado en la nieve. Luego, sacan todo el material necesario (cañas, redes, etc.) para que las maquinas pisanieves puedan trabajar durante la noche y dejar impecables las pistas para el otro día.</p>
