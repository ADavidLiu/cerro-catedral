                    <p class="u-weight-bld u-color-primary">¿Cuándo es necesario contactarse con ellos?</p>
                    <p>Quien tenga la intención de esquiar fuera de las pistas, debe en primer lugar seguir los protocolos de seguridad indicados para esta modalidad</p>
                    <p class="u-weight-bld u-color-primary">(ver Esquí Fuera de Pistas).</p>
                    <p class="u-margin-bottom-med">Es fundamental dirigirse a uno de las Permanencias del Equipo de Patrulla o tomar contacto con un integrante de la Patrulla en las pistas para conocer las condiciones presentes del lugar.</p>
                    <p class="u-margin-bottom-med">Se debe tener en cuenta que la salida de las pistas es exclusivamente responsabilidad de cada uno. Se  recomienda siempre ir con una persona que conozca el terreno y que todo el grupo esté equipado con elementos de seguridad: casco, mochila, pala, A.R.V.A., pastillas RECCO y sonda, todo en correcto funcionamiento.</p>
                    <p class="u-margin-bottom-med">En caso de presenciar o estar involucrado en un accidente nunca mueva a la persona lastimada. Balice la zona para evitar el “sobreaccidente” (que ocurran nuevos perjuicios a los que intervienen en un accidente) y dé aviso inmediatamente a la Patrulla o a cualquier personal identificado de Catedral Alta Patagonia. Luego, sólo siga estrictamente sus indicaciones.</p>
                    <p class="u-margin-bottom-med">Los datos necesarios a informar a la Patrulla son, primero, la identificación y forma de contacto del denunciante; luego, la ubicación exacta del accidentado y, por último, una idea del estado del o los  accidentados.</p>
                    <p>Es importante destacar que los rescates efectuados por personal de Patrulla fuera de las pistas habilitadas y en las áreas fuera de pistas, así como de personas que infrinjan las condiciones generales del servicio o los horarios establecidos para el uso de los medios de elevación, serán realizados con costo a cargo del esquiador usuario. El costo de rescate por persona será, como mínimo, igual al de un pase semanal mayor de alta temporada, y podrá tener un valor mayor de acuerdo a su naturaleza.</p>
