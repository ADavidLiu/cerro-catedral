                    <p class="u-color-black">Catedral Alta Patagonia cuenta con antenas del Sistema Recco para el rastreo de víctimas de avalanchas o personas extraviadas, por lo que se recomienda a los esquiadores contar con esta tecnología. Se trata de un chip con forma de pastilla que viene incorporado a las prendas del esquiador (algunas marcas conocidas lo ofrecen por ejemplo en cascos, pantalones, botas, etc.). Normalmente en cada prenda va un mínimo de dos, para tener mejor posibilidades de rastro. El chip no emite señal y por tanto no requiere de fuente de energía, y al ser de metal es prácticamente indestructible. Funciona al ser detectado mediante una antena especial por parte de los rescatadores.</p>
